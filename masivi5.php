<?php
// Создаем ассоциативный массив с информацией о продуктах (название продукта => цена)
$products = array(
    "Молоко" => 50,
    "Хлеб" => 30,
    "Яблоки" => 80,
    "Кофе" => 150,
    "Масло" => 70
);

// Инициализируем переменные для хранения информации о товаре с самой высокой ценой
$max_price_product = "";
$max_price = 0;

// Находим товар с самой высокой ценой
foreach ($products as $product => $price) {
    if ($price > $max_price) {
        $max_price = $price;
        $max_price_product = $product;
    }
}

// Выводим информацию о товаре с самой высокой ценой
echo "Товар с самой высокой ценой: $max_price_product (цена: $max_price)";
?>
